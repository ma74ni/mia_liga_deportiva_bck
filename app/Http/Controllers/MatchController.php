     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function all()
    {
        $matches = DB::table('matches AS m')
            ->join('teams AS t', 'm.local_team_id', '=', 't.id')
            ->join('teams AS tm', 'm.visit_team_id', '=', 'tm.id')
            ->select('t.name AS local', 'tm.name AS visita')
            ->orderBy('m.number')
            ->get();
        return response()->json($matches);
    }

